static constexpr test_result_t test_cases(test_context_t& context, size_t fn_idx, auto&& test_fn, auto&& expected_of)
{
    test_result_t ret = {
        .status = 0,
        .fn_idx = fn_idx,
        .pair_idx = 0,
    };
    for (; ret.pair_idx < test_pairs.size(); ++ret.pair_idx) {
        auto pair = test_pairs[ret.pair_idx];
        auto expected = expected_of(pair);
        context.size = 0;
        test_fn(pair);
        if (context.size > sizeof(context.data)) {
            ret.status = 3;
            break;
        }
        if (context.size != expected.size()) {
            ret.status = 2;
            break;
        }
        if (!std::equal(expected.begin(), expected.end(), context.data)) {
            ret.status = 1;
            break;
        }
    }
#ifndef NTH_COBS_STATIC_ASSERT_TEST
    if (ret.status) {
        auto expected = expected_of(test_pairs[ret.pair_idx]);
        log_hex(expected.begin(), expected.size(), "expected");
        log_hex(context.data, context.size, "received");
    }
#endif
    return ret;
}

static constexpr test_result_t run()
{
    test_context_t context = {};

    auto base_encoding_callback = [&] (const uint8_t* buf, size_t len) {
        if (context.size + len <= sizeof(context.data)) {
            std::copy_n(buf, len, context.data + context.size);
            context.size += len;
        }
    };
    auto base_decoding_callback = [&] (const uint8_t* buf, size_t len, size_t left) {
        if (!left)
            base_encoding_callback(buf, len);
    };
    cobs_decoder_t decoder;
    cobs_encoder_t encoder;

    auto encode_streamer = [&] (test_pair_t pair) {
        encoder.sink(pair.decoded, base_encoding_callback);
        encoder.stop(base_encoding_callback);
    };
    auto encode_callback = [&] (test_pair_t pair) {
        size_t size = cobs_encode(pair.decoded, base_encoding_callback);
        if (size == pair.encoded.size() - 1) {
            context.data[context.size++] = 0;
        }
    };
    auto encode_buffer = [&] (test_pair_t pair) {
        context.size = cobs_encode(pair.decoded, context.data);
        if (context.size == pair.encoded.size() - 1) {
            context.data[context.size++] = 0;
        }
    };

    auto decode_streamer = [&] (test_pair_t pair) {
        decoder.sink(pair.encoded, base_decoding_callback);
    };
    auto decode_callback = [&] (test_pair_t pair) {
        cobs_decode(pair.encoded, base_decoding_callback);
    };
    auto decode_buffer = [&] (test_pair_t pair) {
        context.size = cobs_decode(pair.encoded, context.data);
    };

    auto enc_selector = [] (const test_pair_t& p) { return p.encoded; };
    auto dec_selector = [] (const test_pair_t& p) { return p.decoded; };
    test_result_t ret = {};

    ret = test_cases(context, 1, encode_streamer, enc_selector);
    if (ret.status) return ret;
    ret = test_cases(context, 2, encode_callback, enc_selector);
    if (ret.status) return ret;
    ret = test_cases(context, 3, encode_buffer, enc_selector);
    if (ret.status) return ret;

    ret = test_cases(context, 4, decode_streamer, dec_selector);
    if (ret.status) return ret;
    ret = test_cases(context, 5, decode_callback, dec_selector);
    if (ret.status) return ret; 
    ret = test_cases(context, 6, decode_buffer, dec_selector);
    if (ret.status) return ret;

    return ret;
}
