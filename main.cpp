/**
 * @brief Convert binary to character.
 * 
 * @param b Binary value.
 * @return Character representation.
 */
static constexpr char bin_to_char(uint8_t b)
{
    return "0123456789abcdef"[b & 0xf];
}

/**
 * @brief Print hex nicely with relevant ASCII representation.
 * 
 * @param dat Data to print
 * @param len Length in bytes
 * @param prefix Optional prefix string.
 */
static void log_hex(const void* dat, size_t len, const char* prefix = nullptr)
{
    printf(": %lu bytes : %s\n", len, prefix ? prefix : "");

    if (!dat || !len)
        return;

    auto p = static_cast<const uint8_t*>(dat);

    for (size_t i = 0; i < len; ++i) {

        if (!(i & 15)) {
            putchar('|');
            putchar(' ');
        }
        putchar(bin_to_char(p[i] >> 4));
        putchar(bin_to_char(p[i] & 0xF));
        putchar(' ');
        
        if ((i & 7) == 7)
            putchar(' ');

        if ((i & 15) == 15) {
            putchar('|');
            for (int j = 15; j >= 0; --j) {
                char c = p[i - j];
                putchar(isprint(c) ? c : '.');
            }
            putchar('|');
            putchar('\n');
        }
    }
    int rem = len - ((len >> 4) << 4);
    if (rem) {
        for (int j = (16 - rem) * 3 + ((~rem & 8) >> 3); j >= 0; --j)
            putchar(' ');
        putchar('|');
        for (int j = rem; j; --j) {
            char c = p[len - j];
            putchar(isprint(c) ? c : '.');
        }
        for (int j = 0; j < 16 - rem; ++j)
            putchar('.');
        putchar('|');
        putchar('\n');
    }
}

